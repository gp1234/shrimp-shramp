export async function seedProjectionData(farmId?: string) {
  if (!farmId) {
    const farm = await prisma.farm.findFirst({ orderBy: { createdAt: "asc" } });
    if (!farm) {
      console.log("⚠️  No farms found, skipping projection seed");
      return;
    }
    farmId = farm.id;
  }

  console.log(`\n📊 Seeding projection reference data for farm ${farmId}...`);

  // Clean existing data
  await prisma.feedSupplierTable.deleteMany({ where: { farmId } });
  await prisma.fCAReference.deleteMany({ where: { farmId } });
  console.log("🧹 Cleaned existing projection reference data");

  // ── EXPALSA (460 real data points, 0.004g to 45.4g) ──
  await prisma.feedSupplierTable.createMany({
    data: EXPALSA_TABLE.map((e) => ({
      farmId: farmId!,
      supplierName: "EXPALSA",
      weightGrams: e.weightGrams,
      bwPercent: e.bwPercent,
    })),
  });
  console.log(`✅ EXPALSA: ${EXPALSA_TABLE.length} entries (0.004g→45.4g)`);

  // ── SUPRACAMARON ──
  await prisma.feedSupplierTable.createMany({
    data: SUPRACAMARON_TABLE.map((e) => ({
      farmId: farmId!,
      supplierName: "SUPRACAMARON",
      weightGrams: e.weightGrams,
      bwPercent: e.bwPercent,
    })),
  });
  console.log(`✅ SUPRACAMARON: ${SUPRACAMARON_TABLE.length} entries`);

  // ── CAMDELICAS ──
  await prisma.feedSupplierTable.createMany({
    data: CAMDELICAS_TABLE.map((e) => ({
      farmId: farmId!,
      supplierName: "CAMDELICAS",
      weightGrams: e.weightGrams,
      bwPercent: e.bwPercent,
    })),
  });
  console.log(`✅ CAMDELICAS: ${CAMDELICAS_TABLE.length} entries`);

  // ── FCA Reference ──
  await prisma.fCAReference.createMany({
    data: FCA_REFERENCE.map((r) => ({
      farmId: farmId!,
      weightFrom: r.weightFrom,
      weightTo: r.weightTo,
      expectedWeeklyGrowth: r.expectedWeeklyGrowth,
      weeklyGrowthPercent: r.weeklyGrowthPercent,
      expectedFCA: r.expectedFCA,
    })),
  });
  console.log(`✅ FCA Reference: ${FCA_REFERENCE.length} entries`);

  console.log("📊 Projection seed complete!\n");
}

if (require.main === module) {
  seedProjectionData()
    .catch((e) => {
      console.error("❌ Projection seed failed:", e);
      process.exit(1);
    })
    .finally(async () => {
      await prisma.$disconnect();
    });
}